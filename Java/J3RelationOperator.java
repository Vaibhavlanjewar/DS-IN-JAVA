public class J3RelationOperator {
public static void main(String[] args) {
    // Relational operator 
    System.out.println("Relational operator ");

    // ==  , != , >, < ,>= ,<= 

    // equal to == 
    int a=10;
    int b=10;
    boolean res= a==b; 
    System.out.println(res); 

    //  !=
    res=a!=b;
    System.out.println(res);

    // >
    res=a>b;
    System.out.println(res);

    // <
    res=a<b;
    System.out.println(res);
 
    //  >=
    res=a>=b;
    System.out.println(res);

    // <=
    res=a<b;
    System.out.println(res);

    

}    
}
